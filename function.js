// Normal function
function myFunction() {
  console.log("Welcome!");
  console.log("Apna College");
}
myFunction();

// Greet function
function greet(name) {
  console.log("Hello " + name);
}
greet("Mustafa");

// Add function
function add(a, b) {
  return a + b;
}
let result = add(5, 7);
console.log(result);

// Arrow function
let add2 = (a, b) => a + b;
let answer = add2(5, 4);
console.log(answer);

// Intro function
function myIntro() {
  console.log("My name is Mustafa");
}
myIntro();
